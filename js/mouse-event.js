
// Onclick Event
function show() {
    var show = document.getElementById("success");
    show.style.display = "block";
}

// Doubleclick Event
function show1() {
    var Mess1 = document.getElementById("Mess");
    Mess1.style.display = "none";
    var disp1 = document.getElementById("disp");
    disp1.style.display = "block";
}


// Onmousedown Event
function show2() {
    var Mess22 = document.getElementById("Mess2");
    Mess22.style.display = "none";
    var disp22 = document.getElementById("disp2");
    disp22.style.display = "block";
}

// Onmouseover Event
function show3(obj) {
    obj.style.color = "Red";
}

// Onmouseup Event
function show4(obj) {
    obj.style.color = "Green";
}

// onmouseout Event
function show5(obj) {
    obj.style.color = "Yellow";
}